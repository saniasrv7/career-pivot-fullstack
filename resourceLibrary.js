// Helper function to get relevant resources based on career pivot
export function getRelevantResources(fromRole, toRole) {
  const resourcePool = [];
  
  // Keywords to domain mapping
  const keywords = {
    product: ['productManagement'],
    pm: ['productManagement'],
    analyst: ['dataAnalytics', 'productManagement'],
    data: ['dataAnalytics'],
    analytics: ['dataAnalytics'],
    ux: ['uxDesign'],
    design: ['uxDesign'],
    engineer: ['engineering'],
    developer: ['engineering'],
    technical: ['engineering'],
    manager: ['leadership'],
    lead: ['leadership'],
    director: ['leadership'],
    strategy: ['strategy'],
    business: ['strategy'],
    marketing: ['marketing'],
    growth: ['marketing'],
    sales: ['sales'],
    customer: ['sales'],
    operations: ['operations'],
    project: ['operations']
  };
  
  const toLower = toRole.toLowerCase();
  const fromLower = fromRole.toLowerCase();
  
  // Find relevant domains based on target role
  let relevantDomains = new Set(['careerTransition']); // Always include career transition
  
  for (const [keyword, domains] of Object.entries(keywords)) {
    if (toLower.includes(keyword)) {
      domains.forEach(d => relevantDomains.add(d));
    }
  }
  
  // Also check from role for transferable resources
  for (const [keyword, domains] of Object.entries(keywords)) {
    if (fromLower.includes(keyword) && !toLower.includes(keyword)) {
      domains.forEach(d => relevantDomains.add(d));
    }
  }
  
  // If no specific match, add general domains
  if (relevantDomains.size === 1) {
    relevantDomains.add('productManagement');
    relevantDomains.add('leadership');
  }
  
  // Collect all resources from relevant domains
  relevantDomains.forEach(domain => {
    if (RESOURCE_LIBRARY[domain]) {
      resourcePool.push(...RESOURCE_LIBRARY[domain]);
    }
  });
  
  return resourcePool;
}
